func randomAccount() db.Account {
	return db.Account{
		ID:       util.RandomInt(1, 10000),
		Owner:    util.RandomOwner(),
		Balance:  util.RandomBalance(),
		Currency: util.RandomCurrency(),
	}
}

func requireBodyMatchAccount(t *testing.T, body *bytes.Buffer, account db.Account) {
	data, err := io.ReadAll(body)
	require.NoError(t, err)

	var retrievedAccount db.Account
	err = json.Unmarshal(data, &retrievedAccount)
	require.NoError(t, err)
	require.Equal(t, account, retrievedAccount)
}

func requireBodyMatchAccounts(t *testing.T, body *bytes.Buffer, accounts []db.Account) {
	data, err := io.ReadAll(body)
	require.NoError(t, err)

	var retrievedAccounts []db.Account
	err = json.Unmarshal(data, &retrievedAccounts)
	require.NoError(t, err)
	require.Equal(t, accounts, retrievedAccounts)
}
